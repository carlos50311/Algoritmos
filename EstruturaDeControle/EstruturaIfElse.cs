public class EstruturaIfElse
{
    public static void Executar()
    {
        double nota = 7.0;

        if (nota >= 7.0)
        {
            Console.WriteLine("Aprovado");
            Console.WriteLine("Não fez mais que a sua obrgação!");
        }
        else
        {
            Console.WriteLine("Recuperação");

        }

    }
}
